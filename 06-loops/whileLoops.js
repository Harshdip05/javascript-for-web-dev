// A while loop repeats a block of code as long as a condition is true.
// The condition is checked before the loop runs each time.

let i = 1;
while (i<=5){
    console.log(i);
    i++;
}


let j = 5;
while(j>=1){
    console.log(j);
    j--;
}



let n = 0;
while(n<=20){
    console.log(n);
    n=n+2;
}